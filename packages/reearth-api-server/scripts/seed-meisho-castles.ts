async function main(): Promise<void> {
  const client = createClient(loadConfig());
  const total = CASTLES.length;
  process.stderr.write(`投入予定: ${String(total)} 件 (landmarks / category=城郭)\n`);

  let ok = 0;
  let ng = 0;
  for (const [no, name, pref, lng, lat, desc] of CASTLES) {
    const payload: CmsPayload = {
      title:       { type: 'text',           value: name },
      description: { type: 'textArea',       value: `${desc}。日本100名城 No.${String(no)}。` },
      category:    { type: 'select',         value: '城郭' },
      prefecture:  { type: 'select',         value: pref },
      location:    { type: 'geometryObject', value: { type: 'Point', coordinates: [lng, lat] } },
    };
    try {
      const created = await client.createItem<{ id: string }>('landmarks', payload);
      ok++;
      process.stderr.write(
        `  [${String(ok + ng).padStart(3, ' ')}/${String(total)}] ${created.id}  ${name} (${pref})\n`,
      );
    } catch (err) {
      ng++;
      const msg = err instanceof Error ? err.message : String(err);
      process.stderr.write(
        `  [${String(ok + ng).padStart(3, ' ')}/${String(total)}] FAILED  ${name}: ${msg}\n`,
      );
    }
  }

  process.stderr.write(`\nDone. ok=${String(ok)} ng=${String(ng)}\n`);
  if (ng > 0) process.exit(1);
}

void main();
